# Operadores y expresiones anidadas

print(1 + 1 == 3)  # Igualdad
print(1+1 != 2)  # Desigualdad

print(4 >= 3)  # Mayor o igual que
print(4 <= 3)  # Menor o igual que
print(15 > 3)  # Mayor que
print(8 < 3)  # Menor que


# Not
print("Using Not", not True)

# And
print("Using And", ((1 < 4) & (5 > 2)))

# Or
print("Using or", 1 > 4 or 2 > 3)


